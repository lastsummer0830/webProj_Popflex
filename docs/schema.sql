CREATE TABLE MEMBER (
  MEMBER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  USER_ID VARCHAR2(30 CHAR) NOT NULL,
  PASSWORD VARCHAR2(128 CHAR) NOT NULL,
  NAME VARCHAR2(50 CHAR) NOT NULL,
  EMAIL VARCHAR2(254 CHAR) NOT NULL,
  ROLE CHAR(1) DEFAULT 'U' NOT NULL,
  IS_USE CHAR(1) DEFAULT 'Y' NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  SOCIAL_TYPE VARCHAR2(20 CHAR) DEFAULT 'LOCAL' NOT NULL,
  SOCIAL_ID VARCHAR2(255 CHAR),
  CONSTRAINT PK_MEMBER PRIMARY KEY (MEMBER_ID),
  CONSTRAINT UQ_MEMBER_USER_ID UNIQUE (USER_ID),
  CONSTRAINT UQ_MEMBER_EMAIL UNIQUE (EMAIL),
  CONSTRAINT CK_MEMBER_ROLE CHECK (ROLE IN ('U','A')),
  CONSTRAINT CK_MEMBER_IS_USE CHECK (IS_USE IN ('Y','N')),
  CONSTRAINT CK_MEMBER_SOCIAL_TYPE CHECK (SOCIAL_TYPE IN ('LOCAL', 'NAVER'))
);

CREATE UNIQUE INDEX UQ_MEMBER_NAVER_SOCIAL
ON MEMBER (
  CASE WHEN SOCIAL_TYPE = 'NAVER' THEN SOCIAL_ID END
);

CREATE TABLE MOVIE (
    MOVIE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    KMDB_MOVIE_ID VARCHAR2(50 CHAR) NOT NULL,
    KMDB_MOVIE_SEQ VARCHAR2(50 CHAR) NOT NULL,
    DOCID VARCHAR2(100 CHAR),
    TITLE VARCHAR2(300 CHAR) NOT NULL,
    DIRECTOR_NAME VARCHAR2(200 CHAR),
    COMPANY VARCHAR2(300 CHAR),
    PLOT CLOB,
    RUNTIME NUMBER,
    RATING VARCHAR2(100 CHAR),
    GENRE VARCHAR2(300 CHAR),
    RATING_GRADE VARCHAR2(100 CHAR),
    RELEASE_DATE DATE,
    POSTER_URL VARCHAR2(1000 CHAR),
    VOD_URL VARCHAR2(1000 CHAR),
    KMDB_URL VARCHAR2(1000 CHAR),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_MOVIE PRIMARY KEY (MOVIE_ID),
    CONSTRAINT UQ_MOVIE_KMDB UNIQUE (KMDB_MOVIE_ID, KMDB_MOVIE_SEQ),
    CONSTRAINT CK_MOVIE_RUNTIME CHECK (RUNTIME IS NULL OR RUNTIME >= 0)
);

CREATE TABLE MOVIE_ACTOR (
    MOVIE_ACTOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    MOVIE_ID NUMBER NOT NULL,
    ACTOR_NAME VARCHAR2(100 CHAR) NOT NULL,
    SORT_ORDER NUMBER DEFAULT 1 NOT NULL,
    CONSTRAINT PK_MOVIE_ACTOR PRIMARY KEY (MOVIE_ACTOR_ID),
    CONSTRAINT FK_MOVIE_ACTOR_MOVIE
        FOREIGN KEY (MOVIE_ID)
        REFERENCES MOVIE(MOVIE_ID)
        ON DELETE CASCADE,
    CONSTRAINT UQ_MOVIE_ACTOR UNIQUE (MOVIE_ID, ACTOR_NAME)
);

CREATE TABLE MOVIE_KEYWORD (
    MOVIE_KEYWORD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    MOVIE_ID NUMBER NOT NULL,
    KEYWORD_NAME VARCHAR2(100 CHAR) NOT NULL,
    CONSTRAINT PK_MOVIE_KEYWORD PRIMARY KEY (MOVIE_KEYWORD_ID),
    CONSTRAINT FK_MOVIE_KEYWORD_MOVIE
        FOREIGN KEY (MOVIE_ID)
        REFERENCES MOVIE(MOVIE_ID)
        ON DELETE CASCADE,
    CONSTRAINT UQ_MOVIE_KEYWORD UNIQUE (MOVIE_ID, KEYWORD_NAME)
);

CREATE INDEX IDX_MOVIE_TITLE
ON MOVIE(TITLE);

CREATE TABLE THEATER (
  THEATER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  THEATER_NAME VARCHAR2(100 CHAR) NOT NULL,
  LOCATION VARCHAR2(200 CHAR),
  CONSTRAINT PK_THEATER PRIMARY KEY (THEATER_ID)
);

CREATE TABLE SCREEN (
  SCREEN_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  THEATER_ID NUMBER NOT NULL,
  SCREEN_NAME VARCHAR2(100 CHAR) NOT NULL,
  CONSTRAINT PK_SCREEN PRIMARY KEY (SCREEN_ID),
  CONSTRAINT FK_SCREEN_THEATER FOREIGN KEY (THEATER_ID) REFERENCES THEATER(THEATER_ID) ON DELETE CASCADE
);

CREATE TABLE SEAT (
  SEAT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  SCREEN_ID NUMBER NOT NULL,
  ROW_LABEL VARCHAR2(5 CHAR) NOT NULL,
  COL_NUM NUMBER NOT NULL,
  CONSTRAINT PK_SEAT PRIMARY KEY (SEAT_ID),
  CONSTRAINT FK_SEAT_SCREEN FOREIGN KEY (SCREEN_ID) REFERENCES SCREEN(SCREEN_ID) ON DELETE CASCADE,
  CONSTRAINT CK_SEAT_COL CHECK (COL_NUM >= 1),
  CONSTRAINT UQ_SEAT_POS UNIQUE (SCREEN_ID, ROW_LABEL, COL_NUM)
);

CREATE TABLE SCHEDULE (
  SCHEDULE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  MOVIE_ID NUMBER NOT NULL,
  SCREEN_ID NUMBER NOT NULL,
  START_TIME DATE NOT NULL,
  END_TIME DATE,
  PRICE NUMBER DEFAULT 12000 NOT NULL,
  CONSTRAINT PK_SCHEDULE PRIMARY KEY (SCHEDULE_ID),
  CONSTRAINT FK_SCHEDULE_MOVIE FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE(MOVIE_ID),
  CONSTRAINT FK_SCHEDULE_SCREEN FOREIGN KEY (SCREEN_ID) REFERENCES SCREEN(SCREEN_ID),
  CONSTRAINT CK_SCHEDULE_TIME CHECK (END_TIME IS NULL OR START_TIME < END_TIME),
  CONSTRAINT CK_SCHEDULE_PRICE CHECK (PRICE >= 0),
  CONSTRAINT UQ_SCHEDULE_SCREEN_TIME UNIQUE (SCREEN_ID, START_TIME)
);

CREATE TABLE RESERVATION (
  RESERVATION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  MEMBER_ID NUMBER NOT NULL,
  SCHEDULE_ID NUMBER NOT NULL,
  HEADCOUNT NUMBER NOT NULL,
  STATUS CHAR(1) DEFAULT 'Y' NOT NULL,
  TOTAL_PRICE NUMBER DEFAULT 0 NOT NULL,
  RESERVED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_RESERVATION PRIMARY KEY (RESERVATION_ID),
  CONSTRAINT FK_RES_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID),
  CONSTRAINT FK_RES_SCHEDULE FOREIGN KEY (SCHEDULE_ID) REFERENCES SCHEDULE(SCHEDULE_ID),
  CONSTRAINT CK_RES_HEAD CHECK (HEADCOUNT >= 1),
  CONSTRAINT CK_RES_STATUS CHECK (STATUS IN ('Y','C')),
  CONSTRAINT CK_RES_PRICE CHECK (TOTAL_PRICE >= 0)
);

CREATE TABLE RESERVATION_SEAT (
  RESERVATION_SEAT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  RESERVATION_ID NUMBER NOT NULL,
  SCHEDULE_ID NUMBER NOT NULL,
  SEAT_ID NUMBER NOT NULL,
  CONSTRAINT PK_RESERVATION_SEAT PRIMARY KEY (RESERVATION_SEAT_ID),
  CONSTRAINT FK_RS_RESERVATION FOREIGN KEY (RESERVATION_ID) REFERENCES RESERVATION(RESERVATION_ID) ON DELETE CASCADE,
  CONSTRAINT FK_RS_SCHEDULE FOREIGN KEY (SCHEDULE_ID) REFERENCES SCHEDULE(SCHEDULE_ID),
  CONSTRAINT FK_RS_SEAT FOREIGN KEY (SEAT_ID) REFERENCES SEAT(SEAT_ID),
  CONSTRAINT UQ_RS_SCHEDULE_SEAT UNIQUE (SCHEDULE_ID, SEAT_ID)
);

CREATE TABLE REVIEW (
  REVIEW_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  MOVIE_ID NUMBER NOT NULL,
  MEMBER_ID NUMBER NOT NULL,
  FRESH_YN CHAR(1) NOT NULL,
  PUBLIC_YN CHAR(1) DEFAULT 'Y' NOT NULL,
  CONTENT CLOB NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_REVIEW PRIMARY KEY (REVIEW_ID),
  CONSTRAINT FK_REVIEW_MOVIE FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE(MOVIE_ID) ON DELETE CASCADE,
  CONSTRAINT FK_REVIEW_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
  CONSTRAINT CK_REVIEW_FRESH CHECK (FRESH_YN IN ('Y','N')),
  CONSTRAINT CK_REVIEW_PUBLIC CHECK (PUBLIC_YN IN ('Y','N')),
  CONSTRAINT UQ_REVIEW_MEMBER_MOVIE UNIQUE (MEMBER_ID, MOVIE_ID)
);

CREATE TABLE FRIEND (
  FRIEND_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  MEMBER_A_ID NUMBER NOT NULL,
  MEMBER_B_ID NUMBER NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_FRIEND PRIMARY KEY (FRIEND_ID),
  CONSTRAINT FK_FRIEND_A FOREIGN KEY (MEMBER_A_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
  CONSTRAINT FK_FRIEND_B FOREIGN KEY (MEMBER_B_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
  CONSTRAINT CK_FRIEND_ORDER CHECK (MEMBER_A_ID < MEMBER_B_ID),
  CONSTRAINT UQ_FRIEND_PAIR UNIQUE (MEMBER_A_ID, MEMBER_B_ID)
);

CREATE TABLE DIARY_ENTRY (
  DIARY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  MEMBER_ID NUMBER NOT NULL,
  MOVIE_ID NUMBER NOT NULL,
  RESERVATION_ID NUMBER,
  REVIEW_ID NUMBER,
  WATCH_DATE DATE NOT NULL,
  STAR_RATING NUMBER(2,1),
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  POPCORN_RATING NUMBER(2,1),

  CONSTRAINT PK_DIARY_ENTRY PRIMARY KEY (DIARY_ID),
  CONSTRAINT FK_DIARY_MEMBER
    FOREIGN KEY (MEMBER_ID)
    REFERENCES MEMBER(MEMBER_ID)
    ON DELETE CASCADE,
  CONSTRAINT FK_DIARY_MOVIE
    FOREIGN KEY (MOVIE_ID)
    REFERENCES MOVIE(MOVIE_ID),
  CONSTRAINT FK_DIARY_RESERVATION
    FOREIGN KEY (RESERVATION_ID)
    REFERENCES RESERVATION(RESERVATION_ID)
    ON DELETE SET NULL,
  CONSTRAINT FK_DIARY_REVIEW
    FOREIGN KEY (REVIEW_ID)
    REFERENCES REVIEW(REVIEW_ID)
    ON DELETE SET NULL,
  CONSTRAINT UQ_DIARY_RESERVATION
    UNIQUE (RESERVATION_ID),
  CONSTRAINT CK_DIARY_STAR
    CHECK (STAR_RATING IS NULL OR (STAR_RATING >= 1.0 AND STAR_RATING <= 5.0))
);

CREATE TABLE TAG (
  TAG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  TAG_NAME VARCHAR2(30 CHAR) NOT NULL,
  CONSTRAINT PK_TAG PRIMARY KEY (TAG_ID),
  CONSTRAINT UQ_TAG_NAME UNIQUE (TAG_NAME)
);

CREATE TABLE DIARY_TAG (
  DIARY_ID NUMBER NOT NULL,
  TAG_ID NUMBER NOT NULL,
  CONSTRAINT PK_DIARY_TAG PRIMARY KEY (DIARY_ID, TAG_ID),
  CONSTRAINT FK_DT_DIARY FOREIGN KEY (DIARY_ID) REFERENCES DIARY_ENTRY(DIARY_ID) ON DELETE CASCADE,
  CONSTRAINT FK_DT_TAG FOREIGN KEY (TAG_ID) REFERENCES TAG(TAG_ID)
);








